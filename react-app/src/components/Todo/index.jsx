import React, { useState, useRef } from "react";
import styled from "styled-components";

const Todo = () => {
  //추가 누를때 새로고침 안되도록
  const handleSubmit = (e) => {
    e.preventDefault();
    const nextList = [...List, { id: nextId.current, text }];
    setList(nextList);
    nextId.current++;
  };
  const defaultTodo = [
    {
      id: 1,
      text: "리액트 기초 알기",
    },
  ];

  const [text, setText] = useState("");
  const [list, setList] = useState([]);
  const nextId = useRef(defaultTodo.length + 1);

  const handleDelete = (deleteId) => {
    const nextList = list.filter((item) => item.id !== deleteId);
    setList(nextList);
  };
  return (
    <Layout>
      <Container>
        <Title>일정관리</Title>
        <Form onSubmit={handleSubmit}>
          <InputText
            value={text}
            onChange={(e) => setText(e.target.value)}
            placeholder="할 일을 입력하세요"
          />
          <BtnSubmit ref={nextId}>+</BtnSubmit>
        </Form>
        <Body>
          <List>
            {list.map((todo, i) => (
              <Item key={todo.id}>
                <label>
                  <input type="checkbox" />
                  <Content>{todo.text}</Content>
                </label>
                <BtnDelete onClick={() => handleDelete(todo.id)}>
                  삭제
                </BtnDelete>
              </Item>
            ))}
          </List>
        </Body>
      </Container>
    </Layout>
  );
};

const Layout = styled.div`
  background: #d2d2d3;
  min-height: 100vh;
  display: flex;
  align-items: center;
  justify-content: center;
`;

const Container = styled.div`
  width: 400px;
`;
const Title = styled.div`
  background: #22b8cf;
  text-align: center;
  color: #fff;
  padding: 10px;
  font-size: 1.5rem;
`;
const Form = styled.form`
  display: flex;
  background: #495057;
`;

const InputText = styled.input`
  flex: 1;
  /* 추가 */
  background: none;
  outline: none;
  border: none;
  padding: 0.5rem;
  font-size: 1.125rem;
  line-height: 1.5;
  color: white;
  &::placeholder {
    color: #dee2e6;
  }
`;

const BtnSubmit = styled.button`
  background: none;
  outline: none;
  border: none;
  background: #868e96;
  color: white;
  padding-left: 1rem;
  padding-right: 1rem;
  font-size: 1.5rem;
  display: flex;
  align-items: center;
  cursor: pointer;
  transition: 0.1s background ease-in;
  &:hover {
    background: #adb5bd;
  }
`;

const Body = styled.div`
  background: #fff;
  min-height: 300px;
`;

const List = styled.ul`
  list-style: none;
  padding: 0;
  margin: 0;
`;
const Item = styled.li`
  display: flex;
  justify-content: space-between;
  padding: 10px;
  & + & {
    border-top: 1px solid #d4d0d0;
  }
`;
const Content = styled.span``;
const BtnDelete = styled.button``;

export default Todo;
